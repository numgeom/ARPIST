import numpy as np

_ws_deg = []
_cs_deg = []


def get_fe2_quadrule(deg):
    """Quadrature rule

    Parameters
    ----------
    deg:            integer, degree

    Returns
    ----------
    ws:             n-by-1 array single or double, weights of quadrature points
    cs:             n-by-3 array single or double,
                    natural coordinates of quadrature points
    """
    if deg >= len(_ws_deg):
        for deg0 in range(len(_ws_deg), deg + 1):
            ws0, cs0 = _init_fe2_quadrule(deg0)
            nqp = ws0.shape[0]
            # cs=[ones(nqp,1)-cs(:,1)-cs(:,2), cs]
            cs = np.array(
                [
                    [1.0 - cs0[row1, 0] - cs0[row1, 1], cs0[row1, 0], cs0[row1, 1]]
                    for row1 in range(nqp)
                ]
            )
            _ws_deg.append(ws0)
            _cs_deg.append(cs)

    return _ws_deg[deg], _cs_deg[deg]


